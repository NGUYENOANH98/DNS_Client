    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String domain = jTextField1.getText();
            try {
                ArrayList listIp = dns.DNSC(domain);
                String ip = "";
                int size = listIp.size();
                if(listIp.get(0).equals("Not Exist !")){
                    JOptionPane.showMessageDialog(null, "Not Exist !");
                    jTextArea1.setText("Not Exist !");
                }
                else if(listIp.get(0).equals("No Internet !")){
                        JOptionPane.showMessageDialog(null, "No Internet !");
                        jTextArea1.setText("No Internet !");
                }
                else{
                    for(int i=0;i<size-1;i++){
                        ip += listIp.get(i) + "\n";
                    }
                    ip += listIp.get(size-1);
                    jTextArea1.setText(ip);
                }
            } catch (IOException ex) {
                Logger.getLogger(UI.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER ) {
            String domain = jTextField1.getText();
            try {
                ArrayList listIp = dns.DNSC(domain);
                String ip = "";
                int size = listIp.size();
                if(listIp.get(0).equals("Not Exist !")){
                    JOptionPane.showMessageDialog(null, "Not Exist !");
                    jTextArea1.setText("Not Exist !");
                }
                else if(listIp.get(0).equals("No Internet !")){
                        JOptionPane.showMessageDialog(null, "No Internet !");
                        jTextArea1.setText("No Internet !");
                }
                else{
                    for(int i=0;i<size-1;i++){
                            ip += listIp.get(i) + "\n";
                    }
                    ip += listIp.get(size-1);
                    jTextArea1.setText(ip);
                }
            } catch (IOException ex) {
                Logger.getLogger(UI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jTextField1KeyPressed

